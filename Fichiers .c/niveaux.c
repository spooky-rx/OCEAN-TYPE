/* Place un ennemi dans le premier slot libre du tableau */
void spawn(Ennemi en[], float x, float y, bool fixe, int pv, int cad,
           ALLEGRO_BITMAP *i_volant, ALLEGRO_BITMAP *i_fixe) {
    for (int i = 0; i < NB_ENNEMIS; i++) {
        if (!en[i].actif) {
            en[i] = (Ennemi){x, y, fixe?0:-12, pv, 90, cad, true, fixe,
                             fixe ? i_fixe : i_volant};
            return;
        }
    }
}
